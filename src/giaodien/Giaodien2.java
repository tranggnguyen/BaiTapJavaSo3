    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
        shop Shop = new shop(maHangTxt.getText(), tenHangTxt.getText(), giaTxt.getText(), sizeTxt.getText());// lấy thông tin r bỏ vào sách
        List<shop> listshop = new ArrayList<>();//khởi tạo 1 danh sách
        boolean kt = true;// ktra
        try {
            listshop = dks.layDuLieu("QUAN");//lấy dữ liệu sách
            for (shop sa : listshop) {

                if (sa.getMaHang().equals(Shop.getMaHang())) {//so sánh chữ

                    JOptionPane.showMessageDialog(null, "Đã có mã tài liệu trong data");//in ra màn hình
                    kt = false;// gán ktr bằng false
                    break;//dừng vòng lặp
                }

            }
            if (kt == true) {
                dks.themshop("QUAN", Shop);
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Giaodien1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBTNActionPerformed

