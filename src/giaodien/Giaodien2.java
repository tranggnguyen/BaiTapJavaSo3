    public void layDuLieu() throws SQLException {
        df.setNumRows(0);
        List<shop> listshop = new ArrayList<>();//khởi tạo 1 danh sách
        listshop = dks.layDuLieu("QUAN");//lấy dữ liệu sách
        for (shop s : listshop) {// chạy for để lấy dl sách
            df.addRow(new Object[]{//thêm thứ tự từ bản sách
                s.getMaHang(),
                s.getTenHang(),
                s.getGia(),
                s.getSize(),});
        }
    }

